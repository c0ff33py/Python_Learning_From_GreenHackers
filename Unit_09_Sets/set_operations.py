set_a = {1, 2, 3, 4, 5}
set_b = {3, 4, 5, 6, 7}

# Union (all items)
print(f"Union: {set_a | set_b}")

# Intersection (common items)
print(f"Intersection: {set_a & set_b}")

# Difference (A - B)
print(f"Difference : {set_a - set_b}")

# Symmetric Difference (items not in both)
print("Symmetric Diff: ", set_a ^ set_b)
