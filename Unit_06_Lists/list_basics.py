# Exercise 1 - List basics

# create a list of fruits
fruits = ["apple", "banana", "orange"]

# Access first item(index 0)
print(f"First fruit: {fruits[0]}")  # output: apple

# Access last item using negative index
print(f"Last fruit: {fruits[-1]}")  # output: orange

# Length of the list
print(f"Total fruits: {len(fruits)}")  # output: 3
